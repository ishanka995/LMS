    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            SimpleDateFormat r = new SimpleDateFormat("yyyy-MM-dd");
            String p = pReturndate.getText();
            String q = today.getText();

            Date tobereturn = r.parse(p);
            Date todays = r.parse(q);

            if (q.compareTo(p) >= 0) {

                try {
                    SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                    String date1 = pReturndate.getText();
                    String date2 = today.getText();

                    Date retur = format.parse(date1);
                    Date today = format.parse(date2);

//            if(retur.compareTo(today)>0){}
                    long name = ChronoUnit.DAYS.between(retur.toInstant(), today.toInstant());
                    String days = Long.toString(name);
                    Datecount.setText(days);
                    String dates = Datecount.getText();
                    long total = name * 15;
                    String taotal = Long.toString(total);
                    PenaltyTotal.setText(taotal);

                } catch (ParseException ex) {
                    Logger.getLogger(PenaltyPortal.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "No penlty for this book");
                recieved_btn.setEnabled(false);
                ResetBtn.setEnabled(false);
                PrintBtn.setEnabled(false);

            }
        } catch (ParseException ex) {
            Logger.getLogger(PenaltyPortal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

