    private void PenaltySearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PenaltySearchActionPerformed
        try {
            String bookId = Pbookid.getText();
            Issue issue = IssueM_Controller.SIssueBook(bookId);
            if (issue != null) {
                Pbookid.setText(issue.getBookID());
                pMemberID.setText(issue.getMemberID());
                pmemberName.setText(issue.getMemberName());
                pissuedate.setText(issue.getIssueDate());
                pReturndate.setText(issue.getReturnDate());

            } else {
                JOptionPane.showMessageDialog(this, "This book is not issued");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PenaltyPortal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PenaltyPortal.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_PenaltySearchActionPerformed

