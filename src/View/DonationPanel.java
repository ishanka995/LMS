            String donationID = DonationID.getText();
            String DonationName = DonatorName.getText();
            String telephone = Telephone.getText();
            String email = Email.getText();
            String Bookname = bookName.getText();
            String Isbn = ISBNNo.getText();
            String publisher = Publisher.getText();
            String price = Price.getText();
            
            Donator donator = new Donator(donationID, DonationName, telephone, email, Bookname, Isbn, publisher, price);
            boolean addDonate = Donator_controller.addDonate(donator);
