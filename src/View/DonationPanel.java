                SlipArea.setText(SlipArea.getText() + "Donator Name :" + DonatorName.getText() + "\n\n");
                SlipArea.setText(SlipArea.getText() + "Telephone No :" + Telephone.getText() + "\n\n");
                SlipArea.setText(SlipArea.getText() + "Email address :" + Email.getText() + "\n\n");
                SlipArea.setText(SlipArea.getText() + "Book Name :" + bookName.getText() + "\n\n");
                SlipArea.setText(SlipArea.getText() + "ISBN no :" + ISBNNo.getText() + "\n\n");
                SlipArea.setText(SlipArea.getText() + "Publisher :" + Publisher.getText() + "\n\n");
                SlipArea.setText(SlipArea.getText() + "book Price :" + Price.getText() + "\n\n");
                SlipArea.setText(SlipArea.getText() + "***********************************************************\n");
                SlipArea.setText(SlipArea.getText() + "Thank you for your support.Your kindness make world\n");
                SlipArea.setText(SlipArea.getText() + " beautiful..! Call us on our hotline 0452245879\n");
